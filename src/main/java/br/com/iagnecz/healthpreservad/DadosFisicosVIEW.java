    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// Obter a linha selecionada
    int linhaSelecionada = tabelaDadosFisicos.getSelectedRow();
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Obter o ID da linha selecionada
    Long idSelecionado = (Long) tabelaDadosFisicos.getValueAt(linhaSelecionada, 0);

    // Confirmar exclusão
    int confirmacao = JOptionPane.showConfirmDialog(this, 
        "Tem certeza que deseja excluir este registro?", 
        "Confirmação", JOptionPane.YES_NO_OPTION);

    if (confirmacao == JOptionPane.YES_OPTION) {
        boolean sucesso = excluirPorId(idSelecionado);

        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Registro excluído com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            // Remover a linha da tabela visual
            ((DefaultTableModel) tabelaDadosFisicos.getModel()).removeRow(linhaSelecionada);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o registro.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

