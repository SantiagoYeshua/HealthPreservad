    public void carregarTabelaFisico() {
    EntityManager em = Persistence.createEntityManagerFactory("hpdb").createEntityManager();
    try {
        UsuarioLogado usuarioLogado = UsuarioLogado.getInstancia();
        Long userId = usuarioLogado.getId();
        
        // Consulta para buscar todos os dados
        List<IndicadorSaude> dadosFisicos = listarDadosFisicosPorUsuario(userId);
        
        // Obter o modelo da JTable
        DefaultTableModel model = (DefaultTableModel) tabelaDadosFisicos.getModel();
        model.setRowCount(0); // Limpa a tabela antes de carregar os dados

        // Adiciona os dados ao modelo
        for (IndicadorSaude a : dadosFisicos) {
            model.addRow(new Object[]{a.getId(), a.getUserId(), a.getAltura(), a.getPeso(), a.getPressaoArterial(), a.getGlicemia(), a.getData()});
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao carregar dados: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    } finally {
        em.close();
    }
}
    
    public List<IndicadorSaude> listarDadosFisicosPorUsuario(Long userId) {
    EntityManager em = Persistence.createEntityManagerFactory("hpdb").createEntityManager();
    return em.createQuery(
        "SELECT a FROM IndicadorSaude a WHERE a.userId = :userId", IndicadorSaude.class)
        .setParameter("userId", userId)
        .getResultList();
}
    
    public boolean excluirPorId(Long id) {
    EntityManager em = Persistence.createEntityManagerFactory("hpdb").createEntityManager();
    try {
        em.getTransaction().begin();
        IndicadorSaude dadosFisicos = em.find(IndicadorSaude.class, id); // Buscar o registro pelo ID
        if (dadosFisicos != null) {
            em.remove(dadosFisicos); // Excluir o registro
            em.getTransaction().commit();
            return true;
        } else {
            return false; // Não encontrou o registro
        }
    } catch (Exception e) {
        e.printStackTrace();
        em.getTransaction().rollback();
        return false;
    } finally {
        em.close();
    }
}
    
