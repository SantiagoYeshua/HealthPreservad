    private void boxVerSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxVerSenhaActionPerformed
if (boxVerSenha.isSelected()) {
        txtSenha.setEchoChar((char) 0); // Exibe o texto da senha
        txtConfirmarSenha.setEchoChar((char) 0);
    } else {
        txtSenha.setEchoChar('•'); // Oculta a senha novamente
        txtConfirmarSenha.setEchoChar('•');
    }
    }//GEN-LAST:event_boxVerSenhaActionPerformed

