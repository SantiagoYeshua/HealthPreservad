    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
    String nome = txtNome.getText();
    String email = txtEmail.getText();
    String senha = new String(txtSenha.getPassword());
    String confirmarSenha = new String(txtConfirmarSenha.getPassword());

    if (senha.equals(confirmarSenha)) {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        Usuario novoUsuario = new Usuario(nome, email, senha);
        if (usuarioDAO.registrarUsuario(novoUsuario)) {
            JOptionPane.showMessageDialog(this, "Usuário registrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao registrar o usuário. Verifique se o email já foi usado.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "As senhas não coincidem.");
        }
    }//GEN-LAST:event_registrarButtonActionPerformed

