    private void salvarAtividade() {
        try {
            // Pega os dados dos campos
            String tipoAtividade = fieldTipoAtividade.getText();
            int duracao = Integer.parseInt(fieldDuracao.getText());
            int calorias = Integer.parseInt(fieldCalorias.getText());
            LocalDate data = LocalDate.parse(fieldData.getText(), DateTimeFormatter.ISO_LOCAL_DATE);

            // Pega o ID do usuário logado
            UsuarioLogado usuarioLogado = UsuarioLogado.getInstancia();
            Long userId = usuarioLogado.getId();
            
            if (userId == null) {
                JOptionPane.showMessageDialog(this, "Nenhum usuário está logado.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Cria o objeto Atividade
            AtividadeFisica atividade = new AtividadeFisica();
            atividade.setTipoDeAtividade(tipoAtividade);
            atividade.setDuracao(duracao);
            atividade.setCaloriasQueimadas(calorias);
            atividade.setData(data);
            atividade.setUserId(userId);

            // Salva no banco de dados
            boolean sucesso = salvar(atividade);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Atividade salva com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                dispose(); // Fecha a janela
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar a atividade.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro nos dados inseridos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
        public boolean salvar(AtividadeFisica atividade) {
    EntityManager em = Persistence.createEntityManagerFactory("hpdb").createEntityManager();
        try {
            em.getTransaction().begin();
            em.persist(atividade); // Salva o alimento no banco de dados
            em.getTransaction().commit();
            return true;
        } catch (Exception e) {
            e.printStackTrace();
            em.getTransaction().rollback();
            return false;
        } finally {
            em.close();
        }
    }
    
